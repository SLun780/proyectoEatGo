                        <h1>Politicas de privacidad</h1>
                        <h4>
                        La compania EatGo denominada igual como EatGo se esta rigiedo por las leyes de Mexico
                        Eatgo y sus ailiados tanto para pedir como restaurantes ofrecera un servicio movil o web el cual para poder hacer uso de dicha plataforma
                        tomara cierta informacion que son de privacidad

 

                        Eatgo es propiedad de Eatgo y es operada por dicha compañía. Eatgo podrá obtener, revelar, utilizar, procesar, almacenar y transmitir los Datos Personales del Usuario y procesará y tratará la Información Personal del Usuario de conformidad con este Aviso de Privacidad y con la Ley.

                        

                        <h3>Recopilación de Información Personal</h3>
                        

                        Eatgo podrá recopilar la Información Personal, incluyendo, de manera enunciativa más no limitativa, la siguiente información:
                        

                        <h3>Información Personal</h3>
                        Al Usuario podría serle requerido proporcionar su número de teléfono móvil, nombre, dirección de correo electrónico y otros Datos Personales relevantes en Eatgo. Eatgo reconocerá la identidad del Usuario y prestará al Usuario los servicios ofrecidos en Eatgo.

                        Eatgo recopila dicha información conforme a las leyes y reglamentos para la seguridad tanto personal como de los bienes del Usuario. Si el Usuario no proporciona dicha información, podría no tener acceso total a los Servicios de Eatgo.

                        

                        <h3>Información que el Usuario podrá Proporcionar</h3>
                        

                        El Usuario podrá, a su entera discreción, proporcionar su fotografía, dirección y otros Datos Personales, entre otros datos.

                        

                        <h3>Ubicación</h3>
                        Eatgo recabará la información de ubicación del Usuario siempre que Eatgo se ejecute en primer plano (aplicación abierta y en pantalla) o en segundo plano (aplicación abierta, pero no en pantalla) en el dispositivo del Usuario. Eatgo obtendrá la ubicación del Usuario a través de la dirección IP, GPS, y otros sensores que ofrezcan información relevante (como dispositivos cercanos que proporcionen información a Eatgo, puntos de acceso a Wi-Fi e información de la estación base) para prestar los Servicios de una mejor manera.

                        

                        <h3>Información de Llamadas</h3>
                        Eatgo podrá grabar y almacenar el contenido de las llamadas y los mensajes que el Usuario haga o envíe a través de Eatgo, incluyendo, de manera enunciativa mas no limitativa, la duración de la llamada y el número de teléfono móvil de las partes en comunicación. Eatgo conservará dicha información por medio de la grabación de llamadas, para verificar los hechos y atender las quejas en caso de controversias o accidentes del Usuario.

                        

                        <h3>Estado de las órdenes y operaciones</h3>
                        Eatgo podrá registrar el estado de las órdenes y operaciones del Usuario. Eatgo recaba dicha información conforme a las leyes y reglamentos aplicables en aras de la seguridad personal y de los bienes del Usuario y para contar con pruebas en caso de controversias.

                        

                        <h3>Información de Evaluación</h3>
                        El Usuario podrá evaluar al Socio de Entrega Designado de manera anónima en Eatgo. En caso de que el Usuario incluya durante dicho proceso su Información Personal, ésta podrá ser revelada al Socio de Entrega Designado.

                        

                        <h3>Datos Financieros</h3>
                        Eatgo podrá recabar los Datos Financieros del Usuario para cobrar y distribuir dinero a la cuenta designada del Usuario y para cualquier otro efecto relacionado con la prestación de los Servicios.

                        

                        <h3>Servicio al Cliente</h3>
                        Cuando el Usuario contacte al equipo de servicio al cliente, Eatgo registrará la información de la llamada del Usuario, sus comentarios o sugerencias, el nombre y la información de contacto y cualquier otra información proporcionada por el Usuario por medio de la grabación de llamadas y del registro manual, de manera que Eatgo pueda manejar las disputas, resolver problemas o dar retroalimentación al Usuario. Si el Usuario se niega a que Eatgo recopile dicha información, tal negativa podrá resultar en que nuestro equipo de servicio al cliente no pueda ser capaz de solucionar las disputas, pero no afectará el uso de las funciones comerciales principales de Eatgo por parte del Usuario.

                        

                        <h3>Análisis de Datos</h3>
                        Para mejorar los servicios de Eatgo, Eatgo recopilará la información de acceso del Usuario, incluyendo el contenido de búsqueda, dirección IP, información del dispositivo (incluyendo modelo del dispositivo, código de identificación del dispositivo, sistema operativo y operador de telecomunicaciones) y ubicación, proporcionando así al Usuario servicios más personalizados y convenientes. Eatgo utilizará el mecanismo de almacenamiento del navegador y el cache de datos para recabar y recopilar información en el dispositivo del Usuario.

                        

                        <h3>Servicios de Terceros</h3>
                        Mientras el Usuario haga uso de servicios ofrecidos por terceros, Eatgo recopilará (incluyendo la información proporcionada por el Usuario y la información registrada por el sistema) la Información Personal necesaria del Usuario y la compartirá con el tercero que preste los servicios, de manera que éste pueda proporcionar al Usuario los servicios apropiados. Si el Usuario se niega a que se recopile dicha información, puede que no sea capaz de utilizar los servicios de terceros a través de Eatgo, pero tal negativa no afectará el uso de las funciones comerciales principales de Eatgo por parte el Usuario.

                        

                        <h3>Permisos de Dispositivos</h3>
                        Eatgo podrá solicitar algunos permisos en el dispositivo del Usuario. Se considerará que los Usuarios otorgan permisos de acceso a Internet en el dispositivo cuando accedan o utilicen Eatgo.
